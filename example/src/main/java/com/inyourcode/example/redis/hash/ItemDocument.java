package com.inyourcode.example.redis.hash;

/**
 * @author JackLei
 */
public class ItemDocument {
    private int id;
    private int num;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    @Override
    public String toString() {
        return "ItemDocument{" +
                "id=" + id +
                ", num=" + num +
                '}';
    }
}
